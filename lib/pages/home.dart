class HomePage extends StatefulWidget {
  HomePage({Key key}) : super(key: key);

  @override
  _HomePageState createState() => _HomePageState();
}

class _HomePageState extends State<HomePage> {

  final CameraPosition _kGooglePlex = CameraPosition(
    target: LatLng(37.42796133580664, -122.085749655962),
    zoom: 14.4746,
  );

  StreamSubscription<Position> _positionStream;

  @override
  void initState() {
    super.initState();
    _startTracking();
  }

  _startTracking(){
    final geolocator = Geolocator();
    final locationOptions = LocationOptions(accuracy: LocationAccuracy.high, distanceFilter: 5);

     _positionStream = geolocator.getPositionStream(locationOptions).listen(_onLocationUpdate);
  }

  _onLocationUpdate(Position position){
    if(position != null){
      print('position ${position.latitude}, ${position.longitude}');
    }
  }

  @override
  void dispose() {
    if(_positionStream != null){
      _positionStream.cancel();
      _positionStream = null;
    }
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
       body: Container(
         width: double.infinity,
         height: double.infinity,
         child: Stack(
           children: <Widget>[
             GoogleMap(
               initialCameraPosition: _kGooglePlex,
               myLocationButtonEnabled: true,
               myLocationEnabled: true,
               onTap: (LatLng p){
                 print('p: ${p.latitude},${p.longitude}');
               },
               onMapCreated: (GoogleMapController controller){
                 controller.setMapStyle(jsonEncode(mapStyle));
               },
             )
           ],
         ),
       ),
    );
  }
}